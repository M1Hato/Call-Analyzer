class CallAnalysis(BaseModel):
    transcription: str = Field(description="Повна текстова транскрибація.")
    call_purpose: str = Field(description="Тип звернення (наприклад: Авто в роботі, Запис на ТО).")

    intro_done: int = Field(description="Початок розмови, представлення (1 або 0).")
    car_body_asked: int = Field(description="Дізнався кузов автомобіля (1 або 0).")
    car_year_asked: int = Field(description="Дізнався рік автомобіля (1 або 0).")
    car_mileage_asked: int = Field(description="Дізнався пробіг (1 або 0).")
    diagnostic_offered: int = Field(description="Пропозиція про комплексну діагностику (1 або 0).")
    previous_works_asked: int = Field(description="Дізнався які роботи робилися раніше (1 або 0).")
    goodbye_done: int = Field(description="Завершення розмови, прощання (1 або 0).")

    call_result: str = Field(
        description="Результат розмови. Строго один з варіантів: 'Запис', 'Повторно консультація', 'Передано іншому філіалу', 'Передзвонити', 'Інше'.")
    booking_date: str = Field(
        description="Якщо результат 'Запис', витягни дату та час запису з розмови (наприклад, '25.08 о 14:00'). Якщо запису немає, поверни порожній рядок ''.")

    selected_work: str = Field(
        description=f"Вибери ОДНУ назву роботи зі списку Топ-100 робіт: {', '.join(TOP_100_WORKS)}. Якщо роботи немає в списку, поверни 'інший варіант'.")
    followed_top100_instructions: int = Field(
        description="Чи дотримувався всіх інструкцій/доп. рекомендацій до цієї роботи з топ 100 робіт. Поверни 1 або 0.")
    failed_recommendations: str = Field(
        description="Яких рекомендацій менеджер не дотримувався стосовно обраної роботи. Напиши коротким текстом, наприклад: 'Не запропонував заміну пильовиків та відбійників'. Якщо все дотримано, поверни порожній рядок.")

    ai_comment: str = Field(description="Детальний коментар про помилки або хід розмови.")
    is_ok: bool = Field(
        description="True якщо все ок, False якщо є критичні помилки або загальний бал дуже низький (менше 4).")

    @property
    def total_score(self) -> int:
        return (
                self.intro_done +
                self.car_body_asked +
                self.car_year_asked +
                self.car_mileage_asked +
                self.diagnostic_offered +
                self.previous_works_asked +
                self.goodbye_done +
                self.followed_top100_instructions
        )


def analyze_audio_with_gemini(audio_file_path: str) -> CallAnalysis:
    client = genai.Client(api_key=settings.GEMINI_API_KEY)

    print(f"[{audio_file_path}] Завантаження аудіофайлу в Gemini API...")
    audio_file = client.files.upload(file=audio_file_path)

    prompt = """
        Ти — досвідчений QA відділу продажів автосервісу. Твоє завдання — проаналізувати розмову менеджера з клієнтом.

        1. Оціни базовий чек-лист (всюди став 1 або 0).
        2. Визнач результат дзвінка. Якщо це 'Запис', обов'язково знайди дату і час.
        3. Знайди, яка послуга з Топ-100 робіт обговорювалася. Якщо менеджер при заміні деталей забув запропонувати супутні матеріали чи послуги (наприклад, при заміні амортизаторів — пильовики, при заміні масла — фільтри, або додаткову діагностику підвіски), зафіксуй це в полі про не отримані рекомендації та постав 0 в полі `followed_top100_instructions`.
        """

    print(f"[{audio_file_path}] Очікування аналізу від Gemini (2.5-flash)...")

    response = client.models.generate_content(
        model='gemini-2.5-flash',
        contents=[audio_file, prompt],
        config=types.GenerateContentConfig(
            response_mime_type="application/json",
            response_schema=CallAnalysis,
            temperature=0.1,
        ),
    )

    client.files.delete(name=audio_file.name)
    return CallAnalysis.model_validate_json(response.text)